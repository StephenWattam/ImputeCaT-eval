  TOKENISER_DELIMITERS = /[ \r 	.,;:'\"()?!]/

  attr_reader :count

  # Create a new factory for classifier-compatible ARFF
  def initialize(name = SecureRandom.hex, class_enum = CLASS_ENUM)
    @relation = Rarff::Relation.new(name)
    @class_enum = class_enum
    
    @relation.attributes << Rarff::Attribute.new('@@class@@', "{{#{@class_enum.join(',')}}}")

    VECTOR_ITEMS.keys.each do |k|
      @relation.attributes << Rarff::Attribute.new(k.to_s, 'NUMERIC')
    end

    @count = 0
    # puts "Loaded #{VECTOR_ITEMS.length} features, #{@class_enum.length} classes."
  end

  # Add a document to the relation
  def add_document(cls, string)
    fail "Class is not declared in header: #{cls}" unless cls == '?' || @class_enum.include?(cls)

    vector = vectorise(string, VECTOR_ITEMS, TOKENISER_DELIMITERS)
    @relation.instances << [cls] + vector.values
    @count += 1
  end

  
  # Convert a string into a vector.
  def vectorise(str, features = VECTOR_ITEMS, delimiters = TOKENISER_DELIMITERS)
    features = features.inject({}) {|x, a| x[a] = 1; x} if features.is_a?(Array)
    hash   = {}
    tokens = str.split(TOKENISER_DELIMITERS)

    # Fill in any missing items
    VECTOR_ITEMS.keys.each do |t|
      hash[t] ||= 0
    end

    # semi-efficiently scan for hits
    (tokens || []).each do |t|
      hash[t]  += 1 if VECTOR_ITEMS[t]
    end

    return hash
  end

  # return the output string from ARFF building
  def output
    @relation.to_arff
  end

  # Return list of classes
  def classes
    @class_enum
  end

end


