class WEKABayesianMultinomial < GenreClassifier

  require 'securerandom'
  require 'fileutils'

  MODEL_FILE_NAME = 'bayesmulti68.model' 
  WEKA_JAR_NAME   = 'weka.jar'


  # java -cp weka.jar weka.classifiers.bayes.NaiveBayesMultinomial -T ../test_test.arff -l ../bnc_68_naive_bayes_multinomial.model -c first -p 0

  def initialize(java_path         = `which java`.strip, 
                 resource_path     = File.join(File.dirname(__FILE__), '../classifier_data/weka_resources/'),
                 stoplist_filename = nil)
    super(stoplist_filename)
    @java_path     = java_path

    @model_path    = File.join(resource_path, MODEL_FILE_NAME)
    @weka_jar_path = File.join(resource_path, WEKA_JAR_NAME)

    # TODO: error if resource path/model doesn't exist.
  end

  def classify(str)
    # Create ARFF file from str
    filename = create_temp_arff(str)

    # run java and grab response
    # puts "#{@java_path} -cp \"#{@weka_jar_path}\" weka.classifiers.bayes.NaiveBayesMultinomial -T \"#{filename}\" -c first -p 0"
    results = `#{@java_path} -cp "#{@weka_jar_path}" weka.classifiers.bayes.NaiveBayesMultinomial -T "#{filename}" -l #{@model_path} -c first -p 0`
  
    # Delete file
    FileUtils.rm(filename)

    return nil unless m = results.match(/\s+1\s+\d+:[^\w]+?\s+(?<group>\d+):/)
    
    # Read class from RX and look up
    cls = m[:group].to_i - 1  # Weka counts from 1, like visual basic or some shit.
    return ARFFFactory.classes[cls]
  end

  def save_state(filename)
    # STUB
  end

  def self.load(filename, *args)
    WEKABayesianMultinomial.new(*args)
  end

  private

  
  # Creates a temporary ARFF file
  # in /tmp/ and returns the filename
  def create_temp_arff(str)
    # Create new relation
    arff = ARFFFactory.new
    arff.add_document('?', str) 

    # Create new temp file
    # Note that we can't use Tempfile because WEKA
    # requires a .arff extension
    filename = File.join('/tmp/', "#{SecureRandom.hex}.arff")
    file = File.open(filename, 'w')
    file.write(arff.output)
    file.close

    return filename
  end
    
